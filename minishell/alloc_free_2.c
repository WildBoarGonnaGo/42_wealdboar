#include "./minishell.h"

void alloc_free_2(void **mem)
{
	int i;

	i = -1;

	while (mem[++i] && mem)
	{
		free(mem[i]);
		mem[i] = NULL;
	}	
	free(mem);
	mem = NULL;
}

